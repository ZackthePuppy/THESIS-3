	<h1>Our Story</h1>

  <p>Cavite  State  University’s  vision  of  being  the  premier university  in  historic  Cavite,  prompted  the  launching  of the  College  of  Business  and  Entrepreneurship  in  Imus  on August 15, 2003. By  virtue  of  the  Memorandum  of  Agreement  among  the Cavite  Provincial  Governor,  Erineo  “Ayong”  Maliksi,  Imus Municipality  Mayor,  Homer  Saquilayan  and  University President,  Dr.  Ruperto  Sangalang,  the  use  of  the  building which  was  originally  proposed  to  be  the  Cavite  Convention and  Trade  Center,  was  granted  to  the  college  for  it  to operate.</p>

  <p>The college started offering  three degree programs, BS in  Business  Management,  Entrepreneurship,  Hotel  and Restaurant  Management  and  two  non-  degree  programs  Diploma in  Entrepreneurship  and  Hotel  and  Restaurant  Management. </p>

  <p>The student population increased annually by 30%.  Other  programs  such  as  BS  in  Computer  Science,  Office  Administration, Information  Technology,  Psychology,  Secondary  Education major  in  English  and  Mathematics,  Certificate  in  Computer Technician,  and  the  latest,  AB  Communication  major  in Journalism were added.</p>

  <p> Seemingly,  linkages  and  partnership  within  the province  and  Metro  Manila  were  established.  Stakeholders and strategic partners are continuously providing their unwavering support.</p>

  <p>In 2012, the college was established as a satellite campus.  It  has  since  then  become  Cavite  State  University Imus Campus  and  has  been  accommodating  the  growing  student population.  There were improvements in the facilities and constructed a 5-storey building.</p>

  <p>Living  up  to  its  commitment  to  produce  globally competitive  and  morally  upright  graduates,  Cavite  State University  Imus  Campus  will  continue  to  offer  exceptional instruction  in  the  achievement  of  academic  excellence, service  to  the  community  and  social  responsibility  in conformity with the university’s aspirations. </p>

  <h1>University Vision</h1>

  <p>The Premier University in historic Cavite recognized for excellence in the development of globally competitive and morally upright individuals.</p>

  <h1>University Mission</h1>

  <p>Cavite State University shall provide excellent, equitable, and relevant educational opportunities in the arts, sciences and technology through quality instruction and responsive research and development activities.

It shall produce professional, skilled and morally upright individuals for global competitiveness. </p>

